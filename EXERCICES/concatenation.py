# Exercice de concaténation

# Données initiales
nom = "Dupont"
prenom = "Alice"
age = 30
ville = "Paris"

# Concaténez les variables pour former une phrase
# Exemple : "Bonjour, je m'appelle Alice Dupont, j'ai 30 ans et je vis à Paris."

phrase = f"Bonjour, je m'appelle {prenom} {nom}, j'ai {age} ans et je vis à {ville}."

# Affichez la phrase résultante
print(phrase)